def shift(input,offset):
    output = []
    for num in input:
        if offset % 2 == 1:
            output.append(num + ( 8 - num % 8 ) * 2  + 1 + 16 * int(offset/2))
        else:
            output.append(num + 16 * int(offset/2))
    output.sort()    
    return output

